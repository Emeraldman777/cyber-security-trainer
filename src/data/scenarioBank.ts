const toScenarioBankItem = (scenario: ResearchScenario): ScenarioBankItem => ({
  id: scenario.id,
  type: scenario.type,
  title: scenario.title,
  category: scenario.category,
  pattern: scenario.patternTags.join(", "),
  channel: scenario.channel,
  manipulation: scenario.patternTags.join(", "),
  targetAudience: "Пользователи CyberGuard",
  situation: scenario.situation,
  classificationQuestion: "Как вы оцениваете ситуацию?",
  classificationOptions,
  steps: scenario.patternTags.map((tag) => ({
    title: tag,
    detail: `Ключевой признак сценария: ${tag}.`,
  })),
  choices: scenario.actions.map((action, index) => ({
    id: `${scenario.id}-A${index + 1}`,
    text: action.text,
    consequence: action.consequence,
    lesson: action.lesson,
    isCorrect: action.correct,
    factors: effectPresets[action.effectPreset],
  })),
  recommendations: scenario.actions
    .filter((action) => action.correct)
    .map((action) => action.lesson),
  sourceRefs: scenario.sourceRefs,
});

export const scenarioBank: ScenarioBankItem[] = researchScenarios.map(toScenarioBankItem);

export const categories = Array.from(new Set(scenarioBank.map((scenario) => scenario.category)));

